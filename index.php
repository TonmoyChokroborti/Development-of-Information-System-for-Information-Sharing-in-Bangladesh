          <h2>About Bangladesh</h2>
          <h3>Introduction</h3>
          <p>Bangladesh has made impressive gains in key human development indicators in recent years. According to the 2018 UNDP Human Development Index Statistical Update, Bangladesh ranks 136th among 189 countries with an HDI score of 0.608, placing it among countries considered to have achieved medium human development. 8 million people have moved out of poverty since 2010. The country is also on track to reach the first Sustainable Development Goal of eradicating extreme poverty by 2030.

But even as Bangladesh has taken these considerable steps towards poverty alleviation, many challenges remain. As of 2016, almost a quarter of the population (24.3%) still live in poverty. The constant threat of shocks – natural, political, or economic - the uncertain impact of globalization, and an increasingly competitive international trade environment impede higher growth rates. In addition, structural changes in rural Bangladesh have spurred rapid economic migration. This exacerbates urban poverty, creates a lack of reliable work and leads to congestion and limited shelter in urban areas. The rate of reduction in urban poverty has decreased in the last few years, only decreasing by 2.4 percentage points (from 21.3% to 18.9%). Bangladesh thus faces considerable challenges to sustain and build on the achievements of the last decade, and to remain on track to meet its targets under the Sustainable Development Goals (SDGs).</p>
<br><br>
<h3>History</h3>
<p>Civilization in the Bengal delta dates back more than 4,300 years. The borders of present-day Bangladesh were established during the British partition of Bengal and India in 1947, when the region became East Pakistan, part of the newly formed state of Pakistan. It was separated from West Pakistan by 1,600 km (994 mi) of Indian territory. Due to a desire for political, economic and linguistic self-determination, popular agitation and civil disobedience grew against the Pakistani state. This culminated in the Bangladesh Liberation War of 1971.

The People’s Republic of Bangladesh was founded as a constitutional, secular, democratic, multiparty, parliamentary republic. After independence, Bangladesh went through periods of poverty and famine, as well as political turmoil and military coups. The restoration of democracy in 1991 has been followed by considerable advances in economic, political, and social development.

Bangladesh straddles the fertile Ganges-Brahmaputra delta, and has a cultural heritage that is proudly intertwined with the broader civilizational history of the Indian subcontinent. It is a pluralistic nation of considerable religious and ethnic diversity. Bangladesh is the world's eighth most populous country and is also one of the most densely populated. The elected parliament in Bangladesh’ parliamentary electoral system is called the Jatiyo Sangsad. Bangladesh is a founding member of SAARC, the Developing 8 Countries, the Bay of Bengal Initiative for Multi-Sectoral Technical and Economic Cooperation (BIMSTEC) and the Bangladesh–China–India–Myanmar Forum for Regional Cooperation (BCIM). It is also a member of the Commonwealth of Nations, the Organization of Islamic Cooperation (OIC) and the Non-Aligned Movement. Bangladesh is also the world's largest contributor to United Nations peacekeeping missions.</p>
<br><br>
<h3>Challenges</h3>
<p>Bangladesh is undergoing substantial economic and social change, and this will intensify in the coming decades. Fundamental forces are at play, involving rapid industrialization, structural change in the economy, and substantial rural-urban migration. These processes bring a host of developmental pressures, and a range of potential inequities. In 2018, Bangladesh met the three UN criteria for LDC graduation, pending two triennial reviews in 2021 and 2024. If the country is successful, it will have to face the challenges of potentially losing preferential market access and other preferential trade concessions. Bangladesh has a centralized governance regime and a complex geography. This poses considerable challenges with regards to rapid urbanization and transboundary issues such as water security. It is vital these inequalities are addressed if poverty is to be further reduced, and a host of future problems associated with social exclusion avoided.

Environmental pressures, exacerbated by climate change, remain significant and could easily worsen if remedial actions are not taken at the local and global level. While the population is expected to stabilize at around 200 million, growing wealth and migration will place further strain on ecosystems and the living environment.

Providing better social services, especially in health and education, is also key to Bangladesh’s continuing ability to meet core welfare objectives. While the country did well in meeting its headline MDG obligations, the quality and durability of some outcomes remains inadequate in the current SDG era. Major service delivery concerns must be addressed by more effectively improving the quality of governance in Bangladesh. As inequalities get more profound and complex, there is a need to look beyond aggregate data to see whether disadvantaged groups get access to the services they need, as well as how performance varies geographically.</p><br><br>
<h3>Successes</h3>
<p>Bangladesh’s economic model has been consistently mindful of the poor and the disadvantaged. Indicators of extreme poverty demonstrate that poverty has fallen from around 50 per cent of the population in 2000, to around 24 per cent in 2016. Broad improvements in social welfare have been secured, and social safety net coverage has improved drastically. This is rooted largely in Bangladesh’s abundant supply of inexpensive labour, and in successful government policies that promote macroeconomic stability and growth. With the global economic recovery, favorable demographics and improving investor confidence, GDP growth has accelerated above 8% in FY2019, with an average of over 6.3% in the last decade.

Bangladesh retains a deep commitment to social solidarity and to a progressive development agenda. The country was one of the most successful development achievers in the MDG era and continues to do well in terms of the SDGs, including being on target to have zero extreme poverty by 2030. The Government has also been successful in identifying issues and using resources efficiently to solve them. This is most clear in relation to maternal mortality, where Bangladesh successfully overcame a significant challenge, and, as of 2015, has brought this figure down to 176 per 100,000 births. This bodes well for future interventions to capitalize on the current successes in the development regime, to further improve access to healthcare and schooling. Investment is another key area for Bangladesh, and the government’s efforts in terms of setting up 100 Special Economic Zones (SEZs) throughout the country, as well as the recent efforts to operationalise One Stop Shop (OSS) for the investors.

Ongoing government programs have targeted disaster preparedness and recovery, with great success. Bangladesh’s vulnerability to disasters is significant, but the country’s track record has been exceptional at improving human security and saving lives. While extreme climatic events still tragically result in some deaths, numbers have fallen drastically. This provides a sound foundation for addressing other pressing questions of environmental sustainability.</p>
