              <p>Bangladesh exports goods worth nearly $40bn and imports goods worth nearly $60bn majority of which carried through seaways. Presently, the country’s two seaports– Chittagong and Mongla—have up to 9.5-metre draft where vessels holding up to 2,000 teu containers can anchor. Bangladesh planned to develop its first deep-sea port as part of a publicprivate partnership with Japan. Matarbari deep seaport will have a 16-metre water draft and will be able to accommodate 8,000 teu post-Panamax vessels, lessening Bangladesh’s dependence on the feeder vessels to ferry export-import goods from the hub ports in Singapore, Colombo and Port Klang. Matarbari deep sea port will be made functional by 2025.</p>
